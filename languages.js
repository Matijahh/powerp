function switchLang(lang) {
  let enLink = document.querySelector(".language-link-en");
  let srLink = document.querySelector(".language-link-sr");
  let enMobileLink = document.querySelector(".mobile-link-en");
  let srMobileLink = document.querySelector(".mobile-link-sr");

  if (lang === "sr") {
    enLink && enLink.classList.remove("active");
    srLink && srLink.classList.add("active");
    enMobileLink && enMobileLink.classList.remove("active");
    srMobileLink && srMobileLink.classList.add("active");
  } else {
    enLink && enLink.classList.add("active");
    srLink && srLink.classList.remove("active");
    enMobileLink && enMobileLink.classList.add("active");
    srMobileLink && srMobileLink.classList.remove("active");
  }

  for (let key in languageContent[lang]) {
    if (key === "name") {
      if (
        document.getElementsByName("name")[0] &&
        document.getElementsByName("name")[1]
      ) {
        document.getElementsByName("name")[0].placeholder =
          languageContent[lang]["name"];
        document.getElementsByName("name")[1].placeholder =
          languageContent[lang]["name"];
      }
    } else if (key === "surname") {
      if (
        document.getElementsByName("surname")[0] &&
        document.getElementsByName("surname")[1]
      ) {
        document.getElementsByName("surname")[0].placeholder =
          languageContent[lang]["surname"];
        document.getElementsByName("surname")[1].placeholder =
          languageContent[lang]["surname"];
      }
    } else if (key === "phone") {
      if (
        document.getElementsByName("phone")[0] &&
        document.getElementsByName("phone")[1]
      ) {
        document.getElementsByName("phone")[0].placeholder =
          languageContent[lang]["phone"];
        document.getElementsByName("phone")[1].placeholder =
          languageContent[lang]["phone"];
      }
    } else if (key === "email") {
      if (
        document.getElementsByName("email")[0] &&
        document.getElementsByName("email")[1]
      ) {
        document.getElementsByName("email")[0].placeholder =
          languageContent[lang]["email"];
        document.getElementsByName("email")[1].placeholder =
          languageContent[lang]["email"];
      }
    } else if (key === "message") {
      if (
        document.getElementsByName("message")[0] &&
        document.getElementsByName("message")[1]
      ) {
        document.getElementsByName("message")[0].placeholder =
          languageContent[lang]["message"];
        document.getElementsByName("message")[1].placeholder =
          languageContent[lang]["message"];
      }
    } else {
      if (document.getElementById(key)) {
        document.getElementById(key).innerHTML = languageContent[lang][key];
      }
    }
  }
}
